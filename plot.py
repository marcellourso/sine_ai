# Carica i dati dal CSV creato con i valori dei gradi e del seno predetto
df = pd.read_csv('valori_seno.csv')

# Estrai i valori dei gradi e del seno predetto
gradi = df['gradi']
seno_predetto = df['seno']

# Calcola i valori del seno reale utilizzando numpy
seno_reale = np.sin(np.radians(gradi))

# Plot dei valori del seno predetto e del seno reale
plt.plot(gradi, seno_predetto, 'r.', label='Seno predetto')
plt.plot(gradi, seno_reale, 'b-', label='Seno reale (sin(x))')
plt.xlabel('Gradi')
plt.ylabel('Valore del seno')
plt.title('Confronto tra seno predetto e seno reale')
plt.legend()
plt.grid(True)
plt.show()

